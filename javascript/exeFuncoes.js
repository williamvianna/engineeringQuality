// Criar uma função
// Que soma 2 números e retorna esses números somado
// Deve ser enviado por parâmetros os 2 números da conta

// Número1: 20
// Número2: 10
// Soma: 30

function soma(numero1, numero2) {
  const resultado = numero1 + numero2;
  console.log("Número1:", numero1);
  console.log("Número2:", numero2);
  console.log("Soma:", resultado);
}

soma(4, 3);
